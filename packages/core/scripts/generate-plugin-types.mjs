import { promises as fs } from "node:fs";
import path from "node:path";

const coreDir = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const repoRoot = path.resolve(coreDir, "../..");
const pluginsDir = path.join(repoRoot, "packages", "plugins");
const generatedDir = path.join(coreDir, "src", "generated");

async function listPlugins() {
  const entries = await fs.readdir(pluginsDir, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => ({
      directory: entry.name,
      id: entry.name.replace(/^plugin-/, ""),
    }))
    .sort((a, b) => a.id.localeCompare(b.id));
}

function toManifestFile(plugins) {
  const items = plugins
    .map((plugin) => `  { id: "${plugin.id}", directory: "${plugin.directory}" },`)
    .join("\n");

  return `/* eslint-disable */\n// AUTO-GENERATED by scripts/generate-plugin-types.mjs\n// DO NOT EDIT MANUALLY.\n\nexport const resolvedPluginManifest = [\n${items}\n] as const;\n\nexport type ResolvedPluginId = (typeof resolvedPluginManifest)[number]["id"];\n`;
}

function toCapabilitiesDts(plugins) {
  const entries = plugins
    .map((plugin) => `  "${plugin.id}": Record<string, unknown>;`)
    .join("\n");

  return `/* eslint-disable */\n// AUTO-GENERATED by scripts/generate-plugin-types.mjs\n\nexport interface PluginCapabilityRegistryShape {\n${entries}\n}\n`;
}

function toRepositoriesDts(plugins) {
  const entries = plugins
    .map((plugin) => `  "${plugin.id}": Record<string, unknown>;`)
    .join("\n");

  return `/* eslint-disable */\n// AUTO-GENERATED by scripts/generate-plugin-types.mjs\n\nexport interface PluginRepositoryRegistryShape {\n${entries}\n}\n`;
}

async function main() {
  const plugins = await listPlugins();
  await fs.mkdir(generatedDir, { recursive: true });

  await fs.writeFile(
    path.join(generatedDir, "plugin-manifest.ts"),
    toManifestFile(plugins),
    "utf8",
  );

  await fs.writeFile(
    path.join(generatedDir, "plugin-capabilities.d.ts"),
    toCapabilitiesDts(plugins),
    "utf8",
  );

  await fs.writeFile(
    path.join(generatedDir, "plugin-repositories.d.ts"),
    toRepositoriesDts(plugins),
    "utf8",
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
